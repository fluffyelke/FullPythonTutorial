print(name.lower())  # string to lower letters

# strip whitespaces
message = '   python     '
print(message)
print(message.rstrip())  # remove whitespaces from the right
print(message.lstrip())  # remove whitespaces from the right
print(message.strip())  # remove whitespaces from left and right
